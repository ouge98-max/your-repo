// Custom logic component for read receipts, now using Lucide icons
export const ReadReceiptIcon: React.FC<{
  status?: 'sending' | 'sent' | 'delivered' | 'read' | 'seen' | 'failed';
  className?: string;
}> = ({ status, className }) => {
  if (status === 'read' || status === 'seen') {
    return <CheckCheck className={`${className} text-blue-500`} />;
  }
  if (status === 'delivered') {
    return <CheckCheck className={`${className} text-muted-foreground`} />;
  }
  if (status === 'sent') {
    return <Check className={`${className} text-muted-foreground`} />;
  }
  return null; // No icon for 'sending', 'failed', or undefined status
};
